        public class War
        {
            public string id;
            public BigInteger regEndBlock;
            public BigInteger regLeftBlocks;//Temporary field, elimiated from Storage 

        }

        public static byte[] WarToByteArray(War war)
        {
            return SD.JoinSegs2Seg(SD.SegString(war.id), SD.SegInt(war.regEndBlock));
        }


        public static War ByteArrayToWar(byte[] data)
        {
            War war = new War();
            war.id = Conv.ByteArray2String(SD.DesegWithIdFromSeg(data, 0));
			war.regEndBlock = Conv.ByteArray2BigInteger(SD.DesegWithIdFromSeg(data, 1));
            BigInteger height = Blockchain.GetHeight();
            war.regLeftBlocks = war.regEndBlock - height;
            return war;
        }


        [Test()]
        public void TestWar()
        {
            War war = new War();
            war.id = "1stBlood";
            war.regEndBlock = 100000;

            byte[] data = WarToByteArray(war);

            War war1 = ByteArrayToWar(data);

            Assert.AreEqual(war.regEndBlock, war1.regEndBlock);
            Assert.AreEqual(war.id, war1.id);

        }
